
import static java.lang.Math.*;
public class Math_function {
    
    public static void main(String[] args) {
        //max(x,y)
        System.out.println("Maximum no = "+Math.max(13,46));

        //min
        System.out.println("Minimum no = "+Math.min(23,34));

        //squareroot
        System.out.println("Square root = "+Math.sqrt(2));

        //abs()
        System.out.println("Absolute value = "+Math.abs(-23));

        //random()
        System.out.println("Random no = "+Math.random());
        
    }
}




